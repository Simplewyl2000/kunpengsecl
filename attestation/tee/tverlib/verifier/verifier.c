// getAkSignFromReport will get the sign_ak[] from report's buffer 
bool getAkSignFromReport(buffer_data report,buffer_data *signak){
   if(report.size<=0){
      printf("report is null");
      return false;
   }
   struct report_response * report_sign;
   report_sign = (struct report_response *)report.buf;
   //get the ak sign 
   uint32_t data_offset;
   uint32_t data_len;
   data_offset = report_sign->params[5].data.blob.data_offset;
   data_len = report_sign->params[5].data.blob.data_len;
   if(sizeof(data_offset)!=0&&sizeof(data_len)!=0){
      signak->buf = report.buf+data_offset+sizeof(uint32_t); //基地址+偏移量+ra_params的tags 4字节
      signak->size = data_len;//签名长度
      return true;
   }
   return false;
}
// getAkCertFromReport will get the akcert[] from report's buffer 
bool getAkCertFromReport(buffer_data report,buffer_data *akcert){
   if(report.size<=0){
      printf("report is null");
      return false;
   }
   struct report_response * report_cert;
   report_cert = (struct report_response *)report.buf;
   //get the akcert 
   uint32_t data_offset;
   uint32_t data_len;
   uint32_t *ak_cert_len;
   uint8_t *ak_cert;
   data_offset = report_cert->params[5].data.blob.data_offset;
   data_len = report_cert->params[5].data.blob.data_len;
   if(sizeof(data_offset)!=0&&sizeof(data_len)!=0){
      ak_cert_len = (uint32_t*)(report.buf+data_offset+data_len);
      ak_cert = report.buf+data_offset+data_len+sizeof(uint32_t);
      akcert->size=*ak_cert_len;
      akcert->buf=ak_cert;
      return true;
   }
   return false;
   
}
