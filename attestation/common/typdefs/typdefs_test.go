func makeBiosLog(t *testing.T) {
	data, err := ioutil.ReadFile("./binary_bios_measurements")
	if err != nil {
		t.Errorf("read err: %v\n", err)
		return
	}
	var buf bytes.Buffer
	buf.WriteString("var biosLog = []byte{\n\t\t")
	for i := 0; i < len(data); i++ {
		s := fmt.Sprintf("0x%02X, ", data[i])
		buf.WriteString(s)
		if i%16 == 15 {
			buf.WriteString("\n\t\t")
		}
	}
	buf.WriteString("\n}\n")
	ioutil.WriteFile("./bioslog.txt", buf.Bytes(), 0755)
}

