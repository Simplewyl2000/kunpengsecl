func TestPcrGroups(t *testing.T) {
	val := "abc"
	na := "filename"
	testCases1 := []struct {
		index int
		value string
		name  string
	}{
		{2, val, na},
		{1, val, na},
		{2, val, na},
		{2, val, na},
		{1, val, na},
	}
	testCases2 := []struct {
		index int
		value string
		name  string
	}{
		{0, "sha256:77e41e1a6e98f7160a8ba85d1b681df84b749f88ffd585612e145421b42ee581", "31002e00300038000000"},
	}
	var buf [24][]byte
	var h1 [24]hash.Hash
	for i := 0; i < 24; i++ {
		buf[i] = make([]byte, 20)
	}
	p := NewPcrGroups()
	for _, c := range testCases1 {
		d, _ := hex.DecodeString(c.value)
		p.ExtendSha1(c.index, d)
		h1[c.index] = sha1.New()
		h1[c.index].Write(buf[c.index])
		h1[c.index].Write(d)
		buf[c.index] = h1[c.index].Sum(nil)
	}
	for i := 0; i < 24; i++ {
		if !bytes.Equal(buf[i], p.Sha1Pcrs[i]) {
			t.Errorf("not match:\n%v\n%v\n", buf[i], p.Sha1Pcrs[i])
		}
	}
	fmt.Printf("sha1=%s\n", p.AggregateSha1(0, 8))
	for i := 0; i < 24; i++ {
		buf[i] = make([]byte, 32)
	}
	for _, c := range testCases2 {
		d, _ := hex.DecodeString(c.value)
		p.ExtendSha256(c.index, d)
		h1[c.index] = sha256.New()
		h1[c.index].Write(buf[c.index])
		h1[c.index].Write(d)
		buf[c.index] = h1[c.index].Sum(nil)
	}
	for i := 0; i < 24; i++ {
		if !bytes.Equal(buf[i], p.Sha256Pcrs[i]) {
			t.Errorf("not match:\n%v\n%v\n", buf[i], p.Sha256Pcrs[i])
		}
	}
	fmt.Printf("sha256=%s\n", p.AggregateSha256(0, 8))
}
